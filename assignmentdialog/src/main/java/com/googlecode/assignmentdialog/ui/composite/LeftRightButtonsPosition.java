package com.googlecode.assignmentdialog.ui.composite;

/**
 * Defines the position of the 'all left', 'left', 'right' and 'all right' buttons on the composite.
 * 
 * @author Kai Winter
 */
public enum LeftRightButtonsPosition {
	/**
	 * The buttons should be displayed between the two tables.
	 */
	MIDDLE,
	/**
	 * The buttons should be displayed below the tables.
	 */
	BOTTOM
}
