package com.googlecode.assignmentdialog.ui.composite.filter;

/**
 * Enum to configure above of which of the left/right table a filter should be shown.
 * 
 * @author Kai Winter
 */
public enum FilterVisible {
	/** No filter should be shown. */
	NONE,
	/** Left filter should be shown. */
	LEFT,
	/** Right filter should be shown. */
	RIGHT,
	/** Both filters should be shown. */
	BOTH;
}
